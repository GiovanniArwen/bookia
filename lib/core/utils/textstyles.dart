import 'package:flutter/material.dart';

class TextStyles {
  static TextStyle size30({Color? color, double? fontSize}) {
    return TextStyle(fontSize: 30, color: color);
  }

  static TextStyle size26({Color? color, double? fontSize}) {
    return TextStyle(fontSize: 26, color: color);
  }

  static TextStyle size16({Color? color, double? fontSize}) {
    return TextStyle(fontSize: 16, color: color);
  }

  static TextStyle size12({Color? color, double? fontSize}) {
    return TextStyle(fontSize: 12, color: color);
  }
}
